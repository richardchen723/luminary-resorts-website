import { getCabinFromHostaway } from "@/lib/cabin-cache"

/**
 * Get cabin by slug, trying Hostaway API first, falling back to static data
 */
export async function getCabinBySlug(slug: string): Promise<Cabin | undefined> {
  // Get static data as fallback
  const staticCabin = cabins.find((cabin) => cabin.slug === slug)
  
  // Try to get from Hostaway first
  try {
    const hostawayCabin = await getCabinFromHostaway(slug)
    if (hostawayCabin) {
      // If Hostaway data doesn't have a subtitle but static data does, use static subtitle
      if (!hostawayCabin.subtitle && staticCabin?.subtitle) {
        hostawayCabin.subtitle = staticCabin.subtitle
      }
      return hostawayCabin
    }
  } catch (error) {
    console.error(`Error fetching cabin ${slug} from Hostaway, using static data:`, error)
  }

  // Fall back to static data
  return staticCabin
}

/**
 * Get cabin by slug synchronously (static data only)
 * Use this when you need synchronous access without API calls
 */
export function getCabinBySlugSync(slug: string): Cabin | undefined {
  return cabins.find((cabin) => cabin.slug === slug)
}

